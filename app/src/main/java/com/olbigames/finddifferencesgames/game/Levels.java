package com.olbigames.finddifferencesgames.game;

public class Levels {
    //private variables
    private int id;
    private int type;
    private int image;
    
   public void setID(int id){
       this.id = id;
   }
   
   public void setType(int type){
       this.type = type;
   }
   
   public void setImage(int image){
       this.image = image;
   }
    
   public int getID(){
       return this.id;
   }

   public int getType(){
       return this.type;
   }
   
   public int getImage(){
       return this.image;
   }
   
}
